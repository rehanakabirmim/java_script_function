
     const result1 = () =>{
        let tMark = document.querySelector(".tMark1").value;
        let pMark = document.querySelector(".pMark1").value;
        let grade = document.querySelector(".grade1");
        let point = document.querySelector(".point1");
        let totalMark = document.querySelector(".totalMark1");
        
        let total = Number(tMark) + Number(pMark);
        totalMark.value = total;
        if( total >= 80 && total <= 100){
            grade.value = "A+";
            point.value = 5.0;
        }
        else if(total >= 70 && total <= 79){
            grade.value = "A";
            point.value = 4.0;
        }
        else if(total >= 60 && total <= 69){
            grade.value = "A-";
            point.value = 3.5;
        }
        else if(total >= 50 && total <= 59){
            grade.value = "B";
            point.value = 3.0;
        }
        else if(total >= 40 && total <= 49){
            grade.value = "C";
            point.value = 2.0;
        }
        else if(total >= 33 && total <= 39){
            grade.value = "D";
            point.value = 1.0;
        }
        else if(total >= 0 && total <= 32){
            grade.value = "F";
            point.value = 0.0;
        }
        else{
            alert("Invalid Input");
        }

    }

